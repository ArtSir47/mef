                        <p class="text-jst">Сутності MEF.DEV забезпечують доступ до бізнес-логіки та конкретної (нативної) взаємодії даних за допомогою Managed Extensibility Framework (MEF). MEF набагато більше, ніж впровадження залежності (Dependency Injection) або інверсія управління (Inversion of Control) - це дає Enterprise-розробникам більш просту настройку і послідовне управління в величезних проектах, не піклуючись про те, коли, де і як налаштований конкретний додаток/плагін/модуль, і незалежно від того, як вони використовують цей додаток/плагін/модуль - отримуючи саме ту реалізацію, яку вони хочуть. Іншими словами, після того, як один розробник публікує додаток на MEF.DEV платформу, інші розробники, що створюють інші плагіни, можуть використовувати даний плагін або його нову версію в реальному режимі часу без перезавантаження - тобто "на льоту".</p>
